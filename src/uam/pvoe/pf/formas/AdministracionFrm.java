    /**
     * Escribe un registro de auditoría en el archivo bitacora.dat
     */
    private void registrarEnBitacora(String accion, String detalles, String motivo) {
        // El parámetro 'true' en FileWriter indica que vamos a AGREGAR al final del archivo (Append)
        try (java.io.BufferedWriter bw = new java.io.BufferedWriter(new java.io.FileWriter("bitacora.dat", true))) {

            // Obtenemos la fecha y hora actual del sistema
            java.time.LocalDateTime ahora = java.time.LocalDateTime.now();
            java.time.format.DateTimeFormatter formato = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
            String fechaHora = ahora.format(formato);

            // Armamos la línea. Si no hay motivo, ponemos "N/A"
            String textoMotivo = (motivo == null || motivo.trim().isEmpty()) ? "N/A" : motivo.trim();
            String linea = "[" + fechaHora + "] ACCIÓN: " + accion + " | DETALLES: " + detalles + " | MOTIVO: " + textoMotivo;

            bw.write(linea);
            bw.newLine();

        } catch (Exception e) {
            System.out.println("Error al escribir en la bitácora: " + e.getMessage());
        }
    }

