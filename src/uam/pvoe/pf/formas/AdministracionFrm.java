    private void tbConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbConsultaMouseClicked
        // TODO add your handling code here:
        int fila = tbConsulta.getSelectedRow();

        if (fila != -1) {
            String idSeleccionado = tbConsulta.getValueAt(fila, 0).toString();

            GestionAsistentes ge = new GestionAsistentes();
            String[] datos = ge.obtenerAsistentePorId(idSeleccionado);

            if (datos != null && datos.length >= 9) {

                txtId.setText(datos[0]);
                txtNombre.setText(datos[1]);
                txtSegundoApellido.setText(datos[3]);
                txtPrimerApellido.setText(datos[2]);
                txtEdad.setText(datos[4]);

                String genero = datos[5];
                if (genero.equalsIgnoreCase("Masculino")) {
                    rbMasculino.setSelected(true);
                } else if (genero.equalsIgnoreCase("Femenino")) {
                    rbFemenino.setSelected(true);
                } else {
                    rbOtro.setSelected(true);
                }

                txtADir.setText(datos[6]);
                txtTelC.setText(datos[7]);
                txtTelE.setText(datos[8]);

                txtAsistente.setText(datos[1] + " " + datos[2] + " " + datos[3]);

                cbTalleres.removeAllItems();

                boolean tieneTalleres = false;
                if (datos.length > 9) {
                    for (int i = 9; i < datos.length; i += 4) {
                        cbTalleres.addItem(datos[i]);
                        tieneTalleres = true;
                    }
                }

                if (!tieneTalleres) {
                    cbTalleres.addItem("Sin talleres inscritos");
                    txtHorario.setText("");
                    txtCosto.setText("");
                    txtCostoMaterial.setText("");
                    txtTotal.setText("");
                    chMaterial.setSelected(false);
                } else {
                    cbTalleres.setSelectedIndex(0);
                }

                pnlDatos.setVisible(true);
                panelTalleres.setVisible(true);
            }
        }
    }//GEN-LAST:event_tbConsultaMouseClicked

    private void cbTalleresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTalleresActionPerformed
        // TODO add your handling code here:
        lblHorariosDisp.setVisible(false);
        cbHorariosDisp.setVisible(false);
        if (cbTalleres.getSelectedItem() == null) {
            return;
        }

        String tallerSeleccionado = cbTalleres.getSelectedItem().toString();

        if (tallerSeleccionado.equals("Sin talleres inscritos")) {
            txtHorario.setText("");
            txtCosto.setText("");
            txtCostoMaterial.setText("");
            txtTotal.setText("");
            chMaterial.setSelected(false);
            return;
        }

        int fila = tbConsulta.getSelectedRow();
        if (fila == -1) {
            return;
        }

        String idSeleccionado = tbConsulta.getValueAt(fila, 0).toString();

        GestionAsistentes ge = new GestionAsistentes();
        String[] datos = ge.obtenerAsistentePorId(idSeleccionado);

        if (datos != null && datos.length > 9) {

            for (int i = 9; i < datos.length; i += 4) {

                if (datos[i].equals(tallerSeleccionado)) {

                    txtHorario.setText(datos[i + 1]);

                    boolean usaMaterial = datos[i + 2].equalsIgnoreCase("Si");
                    chMaterial.setSelected(usaMaterial);

                    txtTotal.setText(datos[i + 3]);

                    GestionTalleres gt = new GestionTalleres();
                    LinkedList<Taller> listaTalleres = gt.listaTalleres();

                    for (uam.pvoe.pf.clases.Taller t : listaTalleres) {
                        if (t.getNombre().equals(tallerSeleccionado)) {

                            txtCosto.setText(String.valueOf(t.getCostoBase()));

                            if (usaMaterial) {
                                txtCostoMaterial.setText(String.valueOf(t.getCostoMaterial()));
                            } else {
                                txtCostoMaterial.setText("0.0");
                            }
                            break;
                        }
                    }
                    break;
                }
            }
        }
    }//GEN-LAST:event_cbTalleresActionPerformed

    private void btnBajaTallerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaTallerActionPerformed
        // TODO add your handling code here:
        if (cbTalleres.getSelectedItem() == null) {
            return;
        }
        String tallerSeleccionado = cbTalleres.getSelectedItem().toString();

        if (tallerSeleccionado.equals("Sin talleres inscritos")) {
            JOptionPane.showMessageDialog(this,
                    "El asistente no tiene talleres para dar de baja.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int fila = tbConsulta.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this,
                    "Por favor seleccione un asistente de la tabla.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String idAsistente = tbConsulta.getValueAt(fila, 0).toString();

        int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de que desea dar de baja al asistente del taller de " + tallerSeleccionado + "?",
                "Confirmar Baja",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (confirmacion == JOptionPane.YES_OPTION) {
            GestionAsistentes ge = new GestionAsistentes();
            boolean exito = ge.darDeBajaTaller(idAsistente, tallerSeleccionado);

            if (exito) {
                JOptionPane.showMessageDialog(this, "Baja realizada con éxito.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                String detalles = "Se dio de baja al ID: " + idAsistente + " del taller: " + tallerSeleccionado;
                registrarEnBitacora("BAJA TALLER", detalles, txtAMotivo.getText());
                txtAMotivo.setText(""); // Limpiamos el motivo para la próxima

                String idActual = idAsistente;
                cargarTodosLosAsistentes();
                actualizarTablaDesdeLista(txtBusca.getText().trim());

                for (int i = 0; i < tbConsulta.getRowCount(); i++) {
                    if (tbConsulta.getValueAt(i, 0).toString().equals(idActual)) {
                        tbConsulta.setRowSelectionInterval(i, i);
                        tbConsultaMouseClicked(null);
                        cbTalleres.setSelectedItem(tallerSeleccionado);
                        break;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error al intentar dar de baja el taller.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBajaTallerActionPerformed

