    private void cargarTodosLosAsistentes() {
        GestionAsistentes ge = new GestionAsistentes();
        listaAsistentesGlobal = ge.obtenerTodosLosAsistentes();

        actualizarTablaDesdeLista("");
    }

    private void actualizarTablaDesdeLista(String criterio) {
        ocultarPaneles();
        tbConsulta.clearSelection();

        DefaultTableModel modelo = (DefaultTableModel) tbConsulta.getModel();
        modelo.setRowCount(0);

        for (Asistente a : listaAsistentesGlobal) {
            String id = a.getId();
            String nombreCompleto = a.getNombre() + " " + a.getPrimerApellido() + " " + a.getSegundoApellido();

            if (criterio.isEmpty() || id.equals(criterio) || nombreCompleto.toLowerCase().contains(criterio.toLowerCase())) {

                modelo.addRow(new Object[]{id, a.getNombre(), a.getPrimerApellido(), a.getSegundoApellido()});
            }
        }

        tbConsulta.revalidate();
        tbConsulta.repaint();
    }

    private void configurarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[][]{},
                new String[]{"ID", "Nombre", "Primer Apellido", "Segundo Apellido"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbConsulta.setModel(modelo);
    }

