    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Taller taller = (Taller) cbTalleres.getSelectedItem();

        if (taller != null && taller.getNombre().compareTo("Seleccione un taller...") != 0) {

            pnlDatos.setVisible(false);

            actualizarTablaDesdeLista(txtBusca.getText().trim());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void cbTalleresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTalleresActionPerformed
        Taller taller = (Taller) cbTalleres.getSelectedItem();

        if (taller != null) {
            if (taller.getNombre().compareTo("Seleccione un taller...") != 0) {

                lblBusca.setVisible(true);
                txtBusca.setVisible(true);
                btnBuscar.setVisible(true);
                jScrollPane1.setVisible(true);
                tbAsistentes.setVisible(true);
                pnlDatos.setVisible(false);

                txtBusca.setText("");

                GestionAsistentes ge = new GestionAsistentes();
                listaFiltrada = ge.obtenerListaPorTaller(taller.getNombre());
                actualizarTablaDesdeLista("");

                this.revalidate();
                this.repaint();

            } else {
                ocultarComponentes();
                txtBusca.setText("");
                actualizarTablaDesdeLista("IMPOSIBLE_QUE_COINCIDA");

                this.revalidate();
                this.repaint();
            }
        }
    }//GEN-LAST:event_cbTalleresActionPerformed

    private void tbAsistentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAsistentesMouseClicked
        // TODO add your handling code here:
        int fila = tbAsistentes.getSelectedRow();

        if (fila != -1) {
            String idSeleccionado = tbAsistentes.getValueAt(fila, 0).toString();

            GestionAsistentes ge = new GestionAsistentes();
            String[] datos = ge.obtenerAsistentePorId(idSeleccionado);

            if (datos != null && datos.length >= 9) {

                txtId.setText(datos[0]);
                txtNombre.setText(datos[1]);
                txtPrimerApellido.setText(datos[2]);
                txtSegundoApellido.setText(datos[3]);
                txtEdad.setText(datos[4]);
                txtGenero.setText(datos[5]);
                txtADir.setText(datos[6]);
                txtTelC.setText(datos[7]);
                txtTelE.setText(datos[8]);

                pnlDatos.setVisible(true);
            }
        }
    }//GEN-LAST:event_tbAsistentesMouseClicked

