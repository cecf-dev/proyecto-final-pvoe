    private void configurarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[][]{},
                new String[]{"ID", "Nombre", "Primer Apellido", "Segundo Apellido"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbAsistentes.setModel(modelo);

    }

    private void ocultarComponentes() {
        lblBusca.setVisible(false);
        txtBusca.setVisible(false);
        btnBuscar.setVisible(false);
        jScrollPane1.setVisible(false);
        tbAsistentes.setVisible(false);
        pnlDatos.setVisible(false);
    }

    public void llenarListaTalleres() {
        cbTalleres.removeAllItems();
        Taller tallerVacio = new Taller("Seleccione un taller...", "", new String[]{""}, 0.0, 0.0);
        cbTalleres.addItem(tallerVacio);

        GestionTalleres administrar = new GestionTalleres();
        LinkedList<Taller> lista = administrar.listaTalleres();

        for (int i = 0; i < lista.size(); i++) {
            Taller taller = lista.get(i);
            cbTalleres.addItem(taller); 
        }
    }

    private void actualizarTablaDesdeLista(String criterio) {
        DefaultTableModel modelo = (DefaultTableModel) tbAsistentes.getModel();
        modelo.setRowCount(0);

        for (Asistente a : listaFiltrada) {
            String id = a.getId();
            String nombreFull = a.getNombre() + " " + a.getPrimerApellido() + " " + a.getSegundoApellido();

            if (criterio.isEmpty() || id.equals(criterio) || nombreFull.toLowerCase().contains(criterio.toLowerCase())) {
                modelo.addRow(new Object[]{id, a.getNombre(), a.getPrimerApellido(), a.getSegundoApellido()});
            }
        }

        tbAsistentes.revalidate();
        tbAsistentes.repaint();
    }

