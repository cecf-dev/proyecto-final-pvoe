                txtInstructor.setVisible(false);
                lblMaterial.setVisible(false);
                chMaterial.setVisible(false);
                lblCosto.setVisible(false);
                txtCosto.setVisible(false);
                lblCostoM.setVisible(false);
                txtCostoMaterial.setVisible(false);
                lblCostoTotal.setVisible(false);
                txtCostoTotal.setVisible(false);
