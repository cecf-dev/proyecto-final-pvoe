    public void llenarListaTalleres() {
        cbTaller.removeAllItems();

        Taller tallerVacio = new Taller("Seleccione un taller...", "", new String[]{""}, 0.0, 0.0);
        cbTaller.addItem(tallerVacio);

        GestionTalleres administrar = new GestionTalleres();
        LinkedList<Taller> lista = administrar.listaTalleres();

        for (int i = 0; i < lista.size(); i++) {
            Taller taller = lista.get(i);
            cbTaller.addItem(taller);
        }
    }

    private void calcularSumaTotal() {
        try {
            String textoCostoBase = txtCosto.getText().trim().isEmpty() ? "0.0" : txtCosto.getText().trim();
            String textoCostoMat = txtCostoMaterial.getText().trim().isEmpty() ? "0.0" : txtCostoMaterial.getText().trim();

            double costoBase = Double.parseDouble(textoCostoBase);
            double costoMat = Double.parseDouble(textoCostoMat);

            double total = costoBase + costoMat;
            txtCostoTotal.setText(String.valueOf(total));

        } catch (NumberFormatException e) {
            txtCostoTotal.setText("0.0");
        }
    }

