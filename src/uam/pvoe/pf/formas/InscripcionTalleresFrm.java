    private void actualizarTablaDesdeLista(String criterio) {
        Compartido.idAsistenteSeleccionado = null;

        tbAsistentes.clearSelection();

        ocultarComponentes();

        DefaultTableModel modelo = (DefaultTableModel) tbAsistentes.getModel();
        modelo.setRowCount(0);

        for (Asistente a : listaAsistentesGlobal) {
            String id = a.getId();
            String nombreCompleto = a.getNombre() + " " + a.getPrimerApellido() + " " + a.getSegundoApellido();
            String telefono = a.getTelContacto();

            if (criterio.isEmpty() || id.equals(criterio) || nombreCompleto.toLowerCase().contains(criterio.toLowerCase())) {
                modelo.addRow(new Object[]{id, a.getNombre(), a.getPrimerApellido(), a.getSegundoApellido(), telefono});
            }
        }

        tbAsistentes.revalidate();
        tbAsistentes.repaint();
    }
