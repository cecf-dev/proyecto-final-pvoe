    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed
        // TODO add your handling code here:
        String idSeleccionado = Compartido.idAsistenteSeleccionado;
        if (idSeleccionado == null || idSeleccionado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un asistente...", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        Taller taller = (Taller) cbTaller.getSelectedItem();
        if (taller == null || taller.getNombre().equals("Seleccione un taller...")) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un taller...", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Recopilar todos los datos de la interfaz
        String nombreTaller = taller.getNombre();
        String horario = cbHorario.getSelectedItem().toString();
        boolean requiereMaterial = chMaterial.isSelected();
        String costoTotal = txtCostoTotal.getText();

        GestionAsistentes ge = new GestionAsistentes();

        String[] datosActuales = ge.obtenerAsistentePorId(idSeleccionado);

        if (datosActuales != null && datosActuales.length > 9) {
            for (int i = 9; i < datosActuales.length; i += 4) {
                if (datosActuales[i].equals(nombreTaller)) {
                    JOptionPane.showMessageDialog(this,
                            "El asistente ya se encuentra inscrito en el taller de " + nombreTaller + ".",
                            "Inscripción Duplicada", JOptionPane.WARNING_MESSAGE);
                    return; // Detenemos la ejecución aquí mismo
                }
            }
        }

        boolean exito = ge.guardarInscripcion(idSeleccionado, nombreTaller, horario, requiereMaterial, costoTotal);

        if (exito) {
            JOptionPane.showMessageDialog(this, "¡Inscripción guardada con éxito!");

            String idActual = Compartido.idAsistenteSeleccionado;

            cargarTodosLosAsistentes();
            actualizarTablaDesdeLista(txtBusca.getText().trim());

            for (int i = 0; i < tbAsistentes.getRowCount(); i++) {
                if (tbAsistentes.getValueAt(i, 0).toString().equals(idActual)) {
                    tbAsistentes.setRowSelectionInterval(i, i);
                    tbAsistentesMouseClicked(null);
                    break;
                }
            }
            cbTaller.setSelectedIndex(0);

        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error al intentar guardar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInscribirActionPerformed

